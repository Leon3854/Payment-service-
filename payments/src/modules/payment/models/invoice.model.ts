import { Field, ObjectType, ID, Float } from '@nestjs/graphql';

/**
 * @ObjectType Invoice
 * @description Финансовое требование (документ), выставляемое пользователю.
 * Служит связующим звеном между подпиской и фактическим платежом для обеспечения Audit Trail.
 */
@ObjectType({ description: 'Финансовое требование (Инвойс)' })
export class Invoice {
  @Field(() => ID)
  id: string;

  @Field(() => Float, { description: 'Сумма к оплате' })
  amountDue: number;

  @Field({ description: 'Статус инвойса (PAID, OPEN, VOID)' })
  status: string;

  @Field({ description: 'Крайний срок оплаты' })
  dueDate: Date;

  @Field()
  createdAt: Date;
}
/**
 * Простыми словами, файл Invoice (Инвойс) — это «Счёт на оплату».
 * Представь что  зашел в ресторан.
 * Подписка (Subscription) — это  намерение пообедать (выбранный ланч-сет).
 * Платеж (Payment) — это сам факт передачи денег или прикладывания карты к терминалу.
 * Инвойс (Invoice) — это (бумажка), который официант кладет на стол ПЕРЕД тем,
 * как ты заплатишь.
 * Зачем он нужен в этом коде:
 * Основание для платежа: В серьезном бизнесе нельзя просто «списать деньги».
 * Сначала система должна выставить документ: «Мы хотим списать 5000 руб. за аренду
 * офиса в марте». Этот документ и есть Инвойс.
 * История долгов: Если у пользователя не прошла карта, Платежа нет. Но Инвойс остается
 * в статусе «OPEN» (Не оплачен). Так система понимает, что пользователь нам должен денег.
 * Без инвойса ты просто потеряешь информацию о том, кто и сколько тебе не доплатил.
 * Периоды (Accounting): Инвойс фиксирует, за какой именно месяц или квартал идет оплата.
 * В нем написано: «Оплата за период с 01.03 по 01.04». Это важно для бухгалтерии и
 * налоговой.
 * Юридическая чистота: Инвойс — это точка контроля. Если возникнет спор («Почему вы
 * списали деньги?»), ты покажешь Инвойс: «Вот счет, он был выставлен 10-го числа,
 * оплата прошла 11-го».
 * Итог: Инвойс — это «связующее звено» между твоим желанием получить деньги (Подпиской)
 * и самим фактом получения денег (Платежом). Без него биллинг превращается в хаос. ✅
 * Как настрой? Теперь, когда понятна «физика» инвойса, можно связать его в Резолвере,
 * чтобы фронтенд мог вывести список счетов пользователя.
 */
