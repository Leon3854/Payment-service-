import { Field, ObjectType, ID, Float } from '@nestjs/graphql';
import { Payment } from './payment.model';

/**
 * ПОНЯТНЫМИ СЛОВАМИ :
 * Этот файл — "Сердце" ваших регулярных доходов. Он следит за тем,
 * когда у клиента закончится оплаченный период и когда пора
 * автоматически списывать деньги снова.
 *
 * ПОЧЕМУ ЭТО ВАЖНО:
 * Он связывает конкретного человека с его тарифным планом и историей
 * всех его оплат. Без него мы бы не знали, кому открывать доступ,
 * а кому — закрывать за неуплату.
 *
 * @ObjectType Subscription
 * @description Сущность, описывающая долгосрочные финансовые отношения с пользователем.
 * Реализует логику SaaS-подписок: отслеживание периодов, статусов и тарифных планов.
 *
 * @description Модель активного или архивного тарифного плана пользователя.
 */
@ObjectType({ description: 'Модель подписки пользователя' })
export class Subscription {
  @Field(() => ID)
  id: string;

  @Field({ description: 'Название тарифного плана' })
  planName: string;

  @Field(() => Float, { description: 'Стоимость подписки' })
  price: number;

  @Field({ description: 'Статус (ACTIVE, CANCELED, PAST_DUE)' })
  status: string;

  @Field({ description: 'Цикл биллинга (MONTHLY, YEARLY)' })
  billingCycle: string;

  @Field({
    nullable: true,
    description: 'Дата окончания текущего оплаченного периода',
  })
  currentPeriodEnd?: Date;

  @Field()
  createdAt: Date;

  // Связанное поле: Платежи этой подписки
  /**
   * Вложенное поле (Relation): История транзакций, привязанных к данной подписке.
   * Позволяет фронтенду запрашивать финансовую историю в один заход (No N+1 Problem).
   */
  @Field(() => [Payment], {
    nullable: 'items',
    description: 'История платежей по данной подписке',
  })
  payments?: Payment[];
}

/**
 * Простыми словами, Subscription (Подписка) — это «Договор на обслуживание».
 * Если Invoice — это счет, а Payment — это чек об оплате, то Subscription — это
 * сама суть ваших отношений с клиентом во времени. Это «двигатель», который
 * говорит системе: «Этот человек с нами надолго, и мы должны брать с него
 * деньги каждый месяц».
 * Зачем этот файл нужен в коде:
 * Право на доступ: Именно здесь система проверяет: «А может ли этот юзер
 * сейчас смотреть фильмы/пользоваться сервисом?». Если статус ACTIVE —
 * двери открыты. Если PAST_DUE (просрочено) — доступ закрыт. ✅
 * Планировщик (Таймер): Поле currentPeriodEnd — это критическая дата. Это как
 * будильник: когда время выйдет, бэкенд проснется и сам создаст новый Invoice
 * на следующий месяц.
 * Тарифный план: Здесь хранится информация о том, на что именно подписан
 * человек (Basic, Premium) и сколько это стоит (price). Без этого файла система
 *  бы не знала, какую сумму вписывать в счет.
 * Связующее звено: Это «корень» всего дерева. В GraphQL через этот файл мы можем
 * увидеть всю историю: «По этой подписке было 12 платежей за прошлый год».
 * Это позволяет фронтенду нарисовать красивый график расходов пользователя.
 * Подписка — это фундамент LTV (Lifetime Value) клиента. Разделяя подписки и
 * платежи, мы можем реализовать гибкую логику: например, позволить пользователю
 * сменить тариф (planName) в середине месяца или поставить подписку на паузу,
 * не удаляя при этом историю его прошлых оплат. Это обеспечивает непрерывность
 * бизнеса и лояльность инвесторов на платформе».
 */
