import { Field, ObjectType, ID, Float } from '@nestjs/graphql';

/**
 * @ObjectType Payment
 * @description Модель финансовой транзакции (фактического движения денежных средств).
 * Служит для фиксации результатов взаимодействия с платежным шлюзом (PSP).
 */
@ObjectType({ description: 'Модель финансовой транзакции' })
export class Payment {
  @Field(() => ID, { description: 'Внутренний уникальный ID транзакции' })
  id: string;

  @Field(() => Float, { description: 'Сумма платежа' })
  amount: number;

  @Field({ description: 'Валюта (RUB/USD)' })
  currency: string;

  @Field({
    description:
      'Статус: SUCCEEDED (успех), PENDING (в обработке), FAILED (ошибка)',
  })
  status: string;

  @Field({
    nullable: true,
    description: 'ID транзакции в платежной системе (ЮKassa/Stripe)',
  })
  externalId?: string;

  @Field({ description: 'Тип провайдера (YOOKASSA, STRIPE)' })
  provider: string;

  @Field({ description: 'Дата и время создания записи' })
  createdAt: Date;
}
/**
 * Простыми словами, Payment — это «Электронный чек» или подтверждение самого факта
 * движения денег. Если Invoice — это «Счёт» (бумажка, которую тебе дали:
 * «Заплати нам 5000»), то Payment — это запись о том, что деньги реально ушли с карты.
 * Зачем этот файл (payment.model.ts) нужен в системе:
 * Связь с банком: В этом файле хранится externalId (ID транзакции в ЮKassa или Stripe).
 * Если возникнет спор, ты берешь этот ID и идешь в поддержку банка: «Вот доказательство,
 *  деньги списаны». ✅
 * Статус операции: Деньги не всегда приходят мгновенно. Платеж может быть:
 * PENDING (пользователь вводит смс-код).
 * SUCCEEDED (деньги упали на счет).
 * FAILED (на карте нет денег).
 * Файл модели позволяет системе понимать: «Мы уже богаты или всё ещё ждем?».
 * Безопасность (Idempotency): Благодаря этой модели мы знаем, что по конкретному
 * заказу (orderId) платеж уже создан. Если юзер нажмет кнопку «Оплатить» 10 раз,
 * мы увидим в базе, что один Payment уже есть, и не спишем деньги 10 раз. 🛡️
 * Метаданные: Там хранится информация: «Оплачено картой МИР, последние 4 цифры 1234».
 * Это нужно для истории в личном кабинете.
 * Твой «бронебойный» аргумент:
 * «Invoice — это то, сколько мы хотим получить.
 * Payment — это то, сколько мы получили по факту.
 * Разделение этих сущностей позволяет нам видеть разрыв в доходах: например, мы
 * выставили инвойсов на 1 000 000 руб., а платежей получили на 800 000.
 * Значит, 200 000 — это наш недополученный доход, по которому нужно запустить
 * Dunning-процесс».
 */
